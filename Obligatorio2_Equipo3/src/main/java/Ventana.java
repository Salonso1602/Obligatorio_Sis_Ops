    private void BotonCreaProcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonCreaProcActionPerformed
        int largoInt = Integer.parseInt(largoIntES.getText());
        int tcpu = Integer.parseInt(cantCpuUser.getText());
        int perInt = Integer.parseInt(periodoIntES.getText());
        int priority = Integer.parseInt(prioridadProc.getText());
        
        Proceso proc = new Proceso(idParaProc.getText(), priority, tcpu, perInt, largoInt);
        
        if (planner != null){
            planner.agregarProcesoAListos(proc);
            displayListos.setText(planner.printListos());
        }
    }//GEN-LAST:event_BotonCreaProcActionPerformed

    private void BotonStartPlanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonStartPlanActionPerformed
        int quant = Integer.parseInt(quantumUser.getText());
        int cpus = Integer.parseInt(cantCpuUser.getText());
        planner = new Planificador(cpus, quant);
    }//GEN-LAST:event_BotonStartPlanActionPerformed

